def resolve_default_value(attr_name, attr_name_zh='', product=None):
    """解析单个属性的通用默认值

    Args:
        attr_name: 属性名（可能含俄语）
        attr_name_zh: 中文名
        product: 商品对象（用于动态填充型号等，可选）

    Returns:
        str: 默认值，未命中返回 None
    """
    defaults = get_default_attribute_values()
    name = (attr_name or '') + ' ' + (attr_name_zh or '')

    for key, val in defaults.items():
        if key in name:
            # 型号类属性优先用商品信息
            if key in ('型号', 'Название модели', 'Артикул производителя', '厂商型号') and product:
                return product.get('mergeCode') or product.get('title', '')[:30] or val
            # 品牌类优先用商品品牌
            if key in ('品牌', 'Бренд', 'бренд', 'Производитель') and product:
                brand = str(product.get('brand') or '').strip()
                if brand and brand.lower() not in ('none', 'copy', '无品牌', 'нет бренда'):
                    return brand
                return val
            return val
    return None



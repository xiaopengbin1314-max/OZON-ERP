def translate_color_to_ru(color_name):
    """将中文颜色名翻译为俄语名（通用层职责）

    Args:
        color_name: 颜色名（中文或俄语，可能含 "白色（белый）" 格式）

    Returns:
        str: 俄语颜色名；无映射则返回清理后的原名
    """
    if not color_name:
        return ''
    # 清理：取括号前的中文部分
    clean = color_name.split('（')[0].split('(')[0].strip().lower()
    # 优先精确匹配
    ru = COLOR_ZH_TO_RU.get(color_name) or COLOR_ZH_TO_RU.get(clean)
    if ru:
        return ru
    # 模糊匹配：包含关系
    for zh, ru_name in COLOR_ZH_TO_RU.items():
        if zh in clean or clean in zh:
            return ru_name
    return clean
